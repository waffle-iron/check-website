function prepOutboundLinks() {
  var allLinks = document.links;
  var links = [];
  for (var i = 0; i < allLinks.length; i++) { 
    if (allLinks[i].rel == "external") {
      allLinks[i].setAttribute('target', '_blank');
    }
  }
};

